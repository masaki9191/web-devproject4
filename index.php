                    phone.addClass("is-invalid");
                    flag = true;
                } else {
                    phone.removeClass("is-invalid");
                }
                if (company.val() == "") {
                    company.addClass("is-invalid");
                    flag = true;
                } else {
                    company.removeClass("is-invalid");
                }

                if (message.val() == "") {
                    message.addClass("is-invalid");
